export async function POST(request: NextRequest) {
  try {
    /* ── Auth check ── */
    const token = request.headers.get('authorization')?.replace('Bearer ', '')
    if (!token) {
      return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })
    }

    const session = await db.userSession.findUnique({
      where: { token },
      include: { user: true },
    })

    if (!session?.user || !['admin', 'super_admin'].includes(session.user.role)) {
      return NextResponse.json({ error: 'Unauthorized' }, { status: 401 })
    }

    const results = {
      categories: { created: 0, skipped: 0 },
      tags: { created: 0, skipped: 0 },
      posts: { created: 0, skipped: 0 },
    }

    /* ── Seed categories ── */
    for (const cat of CATEGORIES) {
      const existing = await db.blogCategory.findUnique({ where: { slug: cat.slug } })
      if (existing) {
        results.categories.skipped++
        continue
      }
      await db.blogCategory.create({
        data: {
          name: cat.name,
          slug: cat.slug,
          description: cat.description,
          color: cat.color,
          icon: cat.icon,
        },
      })
      results.categories.created++
    }

    /* ── Seed tags ── */
    for (const tagName of TAGS) {
      const tagSlug = slugify(tagName)
      const existing = await db.blogTag.findUnique({ where: { slug: tagSlug } })
      if (existing) {
        results.tags.skipped++
        continue
      }
      await db.blogTag.create({
        data: {
          name: tagName,
          slug: tagSlug,
        },
      })
      results.tags.created++
    }

    /* ── Seed posts ── */
    /* Find admin/super_admin user as author */
    const adminUser = await db.user.findFirst({
      where: { role: { in: ['admin', 'super_admin'] } },
    })
    if (!adminUser) {
      return NextResponse.json(
        { error: 'No admin user found to set as post author' },
        { status: 400 }
      )
    }

    /* Fetch all categories and tags for reference */
    const allCategories = await db.blogCategory.findMany()
    const allTags = await db.blogTag.findMany()

    for (const post of SAMPLE_POSTS) {
      const existing = await db.blogPost.findUnique({ where: { slug: post.slug } })
      if (existing) {
        results.posts.skipped++
        continue
      }

      const category = allCategories.find((c) => c.slug === post.categorySlug)
      const postTags = allTags.filter((t) => post.tags.includes(t.name))

      /* Generate a past publishedAt date (spread across last 60 days) */
      const daysAgo = Math.floor(Math.random() * 60)
      const publishedAt = new Date()
      publishedAt.setDate(publishedAt.getDate() - daysAgo)
      publishedAt.setHours(Math.floor(Math.random() * 12) + 8, 0, 0, 0)

      await db.blogPost.create({
        data: {
          title: post.title,
          slug: post.slug,
          content: post.content,
          excerpt: post.excerpt,
          status: 'published',
          readTime: post.readTime,
          isFeatured: post.featured,
          publishedAt,
          authorId: adminUser.id,
          categoryId: category?.id || null,
          tags: {
            create: postTags.map((tag) => ({
              tagId: tag.id,
            })),
          },
        },
      })
      results.posts.created++
    }

    return NextResponse.json({
      success: true,
      message: 'Blog seeded successfully',
      results,
      totalCreated: results.categories.created + results.tags.created + results.posts.created,
    })
  } catch (error) {
    console.error('Blog seed error:', error)
    const message = error instanceof Error ? error.message : 'Unknown error'
    const stack = error instanceof Error ? error.stack : ''
    return NextResponse.json(
      { error: 'Internal server error', debug: message, stack: stack?.substring(0, 500) },
      { status: 500 }
    )
  }
}
