/* ═══════════════════════════════════════════════════════════════════════════ */
/*  Locale Store (external store pattern for useSyncExternalStore)           */
/* ═══════════════════════════════════════════════════════════════════════════ */

let currentLocale: Locale = DEFAULT_LOCALE;
const listeners = new Set<() => void>();

function emitChange() {
  for (const listener of listeners) {
    listener();
  }
}

export function subscribe(listener: () => void) {
  listeners.add(listener);
  return () => {
    listeners.delete(listener);
  };
}

export function getSnapshot(): Locale {
  return currentLocale;
}

function getServerSnapshot(): Locale {
  return DEFAULT_LOCALE;
}

/**
 * Read initial locale from localStorage.
 * Must be called once during app bootstrap (not in render).
 */
export function initLocale(): Locale {
  if (typeof window === 'undefined') return DEFAULT_LOCALE;
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored === 'en' || stored === 'fa') {
      currentLocale = stored;
      applyDirection(currentLocale);
      return stored;
    }
  } catch {
    // localStorage not available
  }
  applyDirection(DEFAULT_LOCALE);
  return DEFAULT_LOCALE;
}

function applyDirection(locale: Locale) {
  if (typeof document === 'undefined') return;
  const dir: Direction = locale === 'fa' ? 'rtl' : 'ltr';
  document.documentElement.dir = dir;
  document.documentElement.lang = locale;
}

export function setLocale(locale: Locale) {
  if (locale === currentLocale) return;
  currentLocale = locale;
  applyDirection(locale);
  // Font switching
  if (locale === 'fa') {
    document.body.style.fontFamily = 'var(--font-vazir)';
  } else {
    document.body.style.fontFamily = 'var(--font-inter), system-ui, sans-serif';
  }
  try {
    localStorage.setItem(STORAGE_KEY, locale);
  } catch {
    // localStorage not available
  }
  emitChange();
}

export function getDirection(locale: Locale): Direction {
  return locale === 'fa' ? 'rtl' : 'ltr';
}

/* ═══════════════════════════════════════════════════════════════════════════ */
/*  Utility: Convert Persian/Arabic digits to English                          */
/* ═══════════════════════════════════════════════════════════════════════════ */

export function toRomanize(text: string): string {
  const persianDigits = '۰۱۲۳۴۵۶۷۸۹';
  return text.replace(/[۰-۹]/g, (d) => String(persianDigits.indexOf(d)));
}

/* ═══════════════════════════════════════════════════════════════════════════ */
/*  Translation Function                                                      */
/* ═══════════════════════════════════════════════════════════════════════════ */

export function translate(locale: Locale, key: string): string {
  return translations[locale][key] ?? key;
}

/* ═══════════════════════════════════════════════════════════════════════════ */
/*  useTranslation Hook                                                       */
/* ═══════════════════════════════════════════════════════════════════════════ */

interface UseTranslationReturn {
  t: (key: string) => string;
  locale: Locale;
  dir: Direction;
  setLocale: (locale: Locale) => void;
}

export function useTranslation(): UseTranslationReturn {
  // Initialize locale on first hook call (client-only)
  useEffect(() => {
    initLocale();
  }, []);

  const locale = useSyncExternalStore(subscribe, getSnapshot, getServerSnapshot);
  const dir = getDirection(locale);

  const t = useCallback(
    (key: string): string => translate(locale, key),
    [locale],
  );

  return { t, locale, dir, setLocale };
}

